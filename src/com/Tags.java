        private void table_update()
        {
            int a;
            
            try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            con = DriverManager.getConnection("jdbc:mysql://localhost/university","root","");
            insert = con.prepareStatement("select * From tag");
            
            ResultSet rs = insert.executeQuery();
            ResultSetMetaData Rss = rs.getMetaData();
            a = Rss.getColumnCount();
            
            DefaultTableModel Df = (DefaultTableModel)jTable1.getModel();
            Df.setRowCount(0);
            
                while(rs.next()) {
                
                Vector vec = new Vector();
                
                for(int i=1; i<=a; i++) {
                    
                    vec.add(rs.getString("id"));
                    vec.add(rs.getString("tagName"));
                    vec.add(rs.getString("tagCode"));
                    vec.add(rs.getString("relateTag"));
                    
                }
                
                Df.addRow(vec);
                  
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Tags.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        catch (SQLException ex) {
            Logger.getLogger(Tags.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        
     private boolean validateFields(){
        if(tag_Name.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Enter TAG NAME");
            
            return false;
        }
        if(tag_Code.getSelectedItem().toString().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Select TAG CODE");
            
            return false;
        }
       if(Relate_Tag.getSelectedItem().toString().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Select RELACTED TAG");
            
            return false;
        }
       return true; 
    }
     
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel Df = (DefaultTableModel)jTable1.getModel();
        int SelectedIndex = jTable1.getSelectedRow();

        tag_Name.setText(Df.getValueAt(SelectedIndex, 1).toString());

        String tagCode = (Df.getValueAt(SelectedIndex, 2).toString());
        switch(tagCode){
            case "select":
            tag_Code.setSelectedIndex(0);
            break;

            case "01":
            tag_Code.setSelectedIndex(1);
            break;

            case "02":
            tag_Code.setSelectedIndex(2);
            break;

            case "03":
            tag_Code.setSelectedIndex(3);
            break;

            case "04":
            tag_Code.setSelectedIndex(4);
            break;

        }

        String relateTag = (Df.getValueAt(SelectedIndex, 3).toString());
        switch(relateTag){
            case "select":
            Relate_Tag.setSelectedIndex(0);
            break;

            case "Lecture":
            Relate_Tag.setSelectedIndex(1);
            break;

            case "Tutorial":
            Relate_Tag.setSelectedIndex(2);
            break;

            case "Labs":
            Relate_Tag.setSelectedIndex(3);
            break;

            case "Assessment":
            Relate_Tag.setSelectedIndex(4);
            break;

        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void clearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clearActionPerformed

        try {
             
              DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
              dtm.setRowCount(0);
                
                
                int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to Clear the Record?", "Warning",JOptionPane.YES_NO_OPTION);
                
                if (dialogResult == JOptionPane.YES_OPTION)
                {
                    Class.forName("com.mysql.cj.jdbc.Driver");
            
                    con= DriverManager.getConnection("jdbc:mysql://localhost/university","root","");
          
                    insert = con.prepareStatement("TRUNCATE TABLE tag");
                    
                    insert.executeUpdate();
            
                    JOptionPane.showMessageDialog(this,"Record Successfully Cleared");
                    table_update();

                    tag_Name.setText("");
                    tag_Code.setSelectedIndex(0);
                    Relate_Tag.setSelectedIndex(0);

                    tag_Name.requestFocus();
                }
            
         
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Tags.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Tags.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_clearActionPerformed

