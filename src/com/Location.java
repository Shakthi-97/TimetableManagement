    private void loctabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loctabMouseClicked
        
        DefaultTableModel Df = (DefaultTableModel)loctab.getModel();
        int selectedIndex = loctab.getSelectedRow();
        
        
        BN.setText(Df.getValueAt(selectedIndex, 1).toString());
        RN.setText(Df.getValueAt(selectedIndex, 2).toString());
        String room_type = Df.getValueAt(selectedIndex, 3).toString();
          
            if(room_type.equals("Lecture Hall")){
                Typelec.setSelected(true);
                Typelab.setSelected(false);
            }
            
            if(room_type.equals("Laboratory")){
                Typelab.setSelected(true);
                Typelec.setSelected(false);
            }
       
        cap.setValue(Integer.parseInt((String)Df.getValueAt(selectedIndex, 4))); 
    }//GEN-LAST:event_loctabMouseClicked

    
    
    
    
    
    
    
    
    
