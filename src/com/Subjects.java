    private void JTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTable1MouseClicked
        // TODO add your handling code here
        
        DefaultTableModel Df = (DefaultTableModel)JTable1.getModel();
        int SelectedIndex = JTable1.getSelectedRow();
        
        String OfferedYear = (Df.getValueAt(SelectedIndex, 1).toString());
        switch(OfferedYear){

            case "select":
            cmb_yr.setSelectedIndex(0);
            break;

            case "1":
            cmb_yr.setSelectedIndex(1);
            break;

            case "2":
            cmb_yr.setSelectedIndex(2);
            break;

            case "3":
            cmb_yr.setSelectedIndex(3);
            break;

            case "4":
            cmb_yr.setSelectedIndex(4);
            break;

        }
        
          String OfferedSemester = Df.getValueAt(SelectedIndex, 2).toString();
          
            if(OfferedSemester.equals("Semester 1")){
                rd_seme1.setSelected(true);
                rd_seme2.setSelected(false);
            }
            
            if(OfferedSemester.equals("Semester 2")){
                rd_seme2.setSelected(true);
                rd_seme1.setSelected(false);
            }
       
        
        txtsubname.setText(Df.getValueAt(SelectedIndex, 3).toString());
        txtsubcode.setText(Df.getValueAt(SelectedIndex, 4).toString());
        spinner_lec.setValue((Integer)Df.getValueAt(SelectedIndex, 5));
        spinner_tute.setValue((Integer)Df.getValueAt(SelectedIndex, 6));
        spinner_lab.setValue((Integer)Df.getValueAt(SelectedIndex, 7));
        spinner_eval.setValue((Integer)Df.getValueAt(SelectedIndex, 8));    
       
    }//GEN-LAST:event_JTable1MouseClicked

