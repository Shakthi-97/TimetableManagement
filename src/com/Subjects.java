        private void table_update(){
            
          int c;
          
          try {
            
            Class.forName("com.mysql.jdbc.Driver");
            
            //con1= DriverManager.getConnection("jdbc:mysql://localhost/university","root","");
            //String url ="jdbc:mysql://timetable-40.mysql.database.azure.com:3306/{university}?useSSL=true&requireSSL=true";
            con1= DriverManager.getConnection("jdbc:mysql://timetable-40.mysql.database.azure.com:3306/university?serverTimezone=UTC", "Janany@timetable-40", "pamathy@25");
          
            insert = con1.prepareStatement("select * From subjects");
            
            ResultSet rs = insert.executeQuery();  
            ResultSetMetaData Rss = rs.getMetaData();
            c = Rss.getColumnCount();
            
            DefaultTableModel Df = (DefaultTableModel)JTable1.getModel();
            Df.setRowCount(0);
            
            while(rs.next()) {
                
                Vector v2 = new Vector();
                
                for(int a=1; a<=c; a++) {
                    
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("offered_yr"));
                    v2.add(rs.getString("offered_seme"));
                    v2.add(rs.getString("subject_name"));
                    v2.add(rs.getString("subject_code"));
                    v2.add(rs.getInt("lec_hrs"));
                    v2.add(rs.getInt("tute_hrs"));
                    v2.add(rs.getInt("lab_hrs"));
                    v2.add(rs.getInt("evaluation_hrs"));
                    
                }
                
                Df.addRow(v2);
                  
            }
         
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Lecturer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Lecturer.class.getName()).log(Level.SEVERE, null, ex);
        }
          
        }

        
        private boolean Subjects_validateFields(){
        if(cmb_yr.getSelectedItem().toString().equals(" "))
        {
            JOptionPane.showMessageDialog(this,"Select the Year");
            cmb_yr.requestFocus();
            
            return false;
        }
        if( rd_seme1.isSelected()==false && rd_seme2.isSelected()==false )
        {
            JOptionPane.showMessageDialog(this,"Select the Semester");
            rd_seme1.requestFocus();
                
            return false;
        }
 
        if(txtsubname.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Enter the Subject Name");
            txtsubname.requestFocus();
            
            return false;
        }
        
        if(txtsubcode.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Enter the Subject Code");
            txtsubcode.requestFocus();
            
            return false;
        }
        
       return true; 
    }
    
