        private void table_update()
        {
            int a;
            
            try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            con = DriverManager.getConnection("jdbc:mysql://localhost/university","root","");
            insert = con.prepareStatement("select * From student");
            
            ResultSet rs = insert.executeQuery();
            ResultSetMetaData Rss = rs.getMetaData();
            a = Rss.getColumnCount();
            
            DefaultTableModel Df = (DefaultTableModel)groupTable.getModel();
            Df.setRowCount(0);
            
                while(rs.next()) {
                
                Vector vec = new Vector();
                
                for(int i=1; i<=a; i++) {
                    
                    vec.add(rs.getString("id"));
                    vec.add(rs.getString("semester"));
                    vec.add(rs.getString("programme"));
                    vec.add(rs.getString("groupNo"));
                    vec.add(rs.getString("groupID"));
                    vec.add(rs.getString("subGroupNo"));
                    vec.add(rs.getString("subGroupID"));
                    
                }
                
                Df.addRow(vec);
                  
            }
            
           
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Group.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        catch (SQLException ex) {
            Logger.getLogger(Group.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
        
   
